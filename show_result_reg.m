function show_result_reg(images,pred,lmx,lmy,predx,predy,id,figure_id)

if nargin>3
    figure(figure_id);
end

names = {'left eye center', ...
         'right eye center', ...
         'left eye inner', ...
         'left eye outer', ...
         'right eye inner', ...
         'right eye outer', ...
         'left eyebrow inner', ...
         'left eyebrow outer', ...
         'right eyebrow inner', ...
         'right eyebrow outer', ...
         'nose tip', ...
         'left mouth', ...
         'right mouth', ...
         'mouth center top', ...
         'mouth center bottom'};

for i=1:size(lmx,1);
subplot(6,5,i);
imshow(fliplr(imrotate(squeeze(max1(pred(:,:,i,id))),-90)));
hold on;
scatter(lmx(i,id),lmy(i,id),'g');
scatter(predx(i,id),predy(i,id),'r');
title(names{i});
end

for i=1:size(lmx,1);
subplot(6,5,size(lmx,1)+i);
imshow(squeeze(images(id,:,:))./255);
hold on;
scatter(lmx(i,id),lmy(i,id),'g');
scatter(predx(i,id),predy(i,id),'r');
title(names{i});
end
